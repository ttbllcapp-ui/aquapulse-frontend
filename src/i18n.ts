const DICTS: Record<LangCode, Dict> = { tr, en, de, fr, es, it, ar, ru: EXTRA_DICTS.ru, ja: EXTRA_DICTS.ja, ko: EXTRA_DICTS.ko, zh: EXTRA_DICTS.zh, hi: EXTRA_DICTS.hi, pt: EXTRA_DICTS.pt, nl: EXTRA_DICTS.nl, pl: EXTRA_DICTS.pl, sv: EXTRA_DICTS.sv, id: EXTRA_DICTS.id, vi: EXTRA_DICTS.vi };

export function tr_fn(lang: LangCode, key: string, vars?: Record<string, string | number>): string {
  const dict = DICTS[lang] || DICTS.tr;
  // Fallback chain: requested lang -> English -> Turkish -> key
  let s = dict[key] || DICTS.en[key] || DICTS.tr[key] || key;
  if (vars) Object.keys(vars).forEach((k) => { s = s.replace(`{${k}}`, String(vars[k])); });
  return s;
}

export function isRTL(lang: LangCode): boolean {
  return lang === 'ar';
}

// Detect device language and return nearest supported
export function detectDeviceLang(locale: string | undefined): LangCode {
  if (!locale) return 'en';
  const short = locale.slice(0, 2).toLowerCase();
  const supported: LangCode[] = ['tr', 'en', 'de', 'fr', 'es', 'it', 'ar', 'ru', 'ja', 'ko', 'zh', 'hi', 'pt', 'nl', 'pl', 'sv', 'id', 'vi'];
  if (supported.includes(short as LangCode)) return short as LangCode;
  return 'en';
}
